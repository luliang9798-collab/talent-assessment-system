const sqlite3 = require('sqlite3').verbose();
const db = new sqlite3.Database('talent_assessment_new.db');

function insertQuestion(toolId, dimension, questionText, optionA, optionB, optionC, optionD, scoreA, scoreB, scoreC, scoreD, orderNum) {
    return new Promise((resolve, reject) => {
        const sql = `INSERT INTO questions (tool_id, question_text, dimension, option_a, option_b, option_c, option_d, score_a, score_b, score_c, score_d, order_num)
                     VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)`;
        db.run(sql, [toolId, questionText, dimension, optionA, optionB, optionC, optionD, scoreA, scoreB, scoreC, scoreD, orderNum], function(err) {
            if (err) reject(err);
            else resolve(this.lastID);
        });
    });
}

