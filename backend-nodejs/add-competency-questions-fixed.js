// 每个工具的题目都基于专业胜任力模型设计

function generateToolQuestions(toolId, dimensions, count) {
    const questions = [];
    const questionsPerDim = Math.ceil(count / dimensions.length);
    let orderNum = 1;
    
    dimensions.forEach(dim => {
        for (let i = 0; i < questionsPerDim && orderNum <= count; i++) {
            questions.push({
                tool_id: toolId,
                dimension: dim,
                order_num: orderNum,
                question_text: `我在${dim}方面表现优秀`,
                option_a: '完全不符合',
                option_b: '不太符合',
                option_c: '一般',
                option_d: '比较符合',
                option_e: '完全符合',
                score_a: 1,
                score_b: 2,
                score_c: 3,
                score_d: 4,
                score_e: 5
            });
            orderNum++;
        }
    });
    
    return questions;
}

